function [] = rerunAgent(handles)
    socInWeight = eval(get(handles.edit2, 'string'));
    momentum = eval(get(handles.edit3, 'string'));
    rewardAmount = eval(get(handles.edit4, 'string'));
    epsilon = eval(get(handles.edit6, 'string'));
    discount = eval(get(handles.edit5, 'string'));
    tableData = get(handles.uitable1, 'Data');
    socInput = tableData(:,2);
    rightAnswers = tableData(:,1);
    
    [agentAnswers, totalReward, rewardVsTime] = runTest(rightAnswers.', socInput.', socInWeight, momentum, rewardAmount, epsilon, discount);
    set(handles.uitable1, 'Data', [rightAnswers socInput agentAnswers.']);
    plot(handles.axes1, rewardVsTime(1,:), rewardVsTime(2,:));


